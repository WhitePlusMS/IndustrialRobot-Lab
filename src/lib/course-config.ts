export type ModuleId = 'robot' | 'camera' | 'grasp';
export type LearningSubTab = 'steps' | 'explain';
export type LearningMode = 'guided' | 'free';

/** 右侧教学引导内容 */
export interface TeachingGuide {
  /** 当前步骤要完成的总任务 */
  task: string;
  /** 操作步骤清单，按顺序执行 */
  operationSteps: string[];
  /** 自我检查项，完成后对照确认 */
  checkItems: string[];
  /** 关键观察点，提示学生看 3D 场景或数据面板 */
  observe: string[];
  /** 本步骤涉及的关键术语及定义 */
  keyTerms: { term: string; definition: string }[];
}

/** 步骤与 3D 场景的交互配置 */
export interface SceneInteraction {
  /** 交互类型 */
  type: 'joint-highlight' | 'camera-focus' | 'object-highlight';
  /** 交互对象标识：joint-highlight 时可为 'all' 或具体关节名 */
  target: string;
  /** 提示文案 */
  hint: string;
}

export interface CourseStep {
  id: string;
  title: string;
  subtitle: string;
  /** 步骤讲解，允许使用简单 HTML 标签 */
  explanation: string;
  /** 本步目标 */
  goal: string;
  /** 注意事项 */
  warning: string;
  /** 操作提示 */
  hint?: string;
  /** 右侧教学引导 */
  teachingGuide: TeachingGuide;
  /** 关联知识点 ID 列表 */
  relatedTheoryIds?: string[];
  /** 步骤与 3D 场景的交互配置 */
  sceneInteraction?: SceneInteraction;
}

export interface TheoryItem {
  /** 知识点唯一标识 */
  id: string;
  title: string;
  description: string;
  /** 为什么要在这个步骤学习该知识点 */
  whyNow: string;
}

export interface CourseModule {
  id: ModuleId;
  title: string;
  progressTitle: string;
  theory: TheoryItem[];
  steps: CourseStep[];
}

